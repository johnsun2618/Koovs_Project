var cartProducts = JSON.parse(localStorage.getItem("cartData")) || [];

displayData(menData);

function displayData(menData) {
    menData.map(function (elem) {
        var product = document.createElement("div");
        product.setAttribute("class","productBox");

        var image = document.createElement("img");
        image.src = elem.imgUrl;
        image.setAttribute("class","productImg");

        var pName = document.createElement("h4");
        pName.innerText = elem.name;

        var pPrice = document.createElement("p");
        pPrice.innerText = 'Rs.' + elem.price;

        var btn1 = document.createElement("button");
        btn1.innerText = 'Add to Cart';
        btn1.addEventListener("click",function(){
            addToCart(elem);
        });

        var btn2 = document.createElement("button");
        btn2.innerText = 'Add to MyList';

        product.append(image, pName, pPrice, btn1, btn2);

        document.querySelector(".products").append(product);
    });
}

function sortByPrice() {
    document.querySelector(".products").innerHTML = "";
    var selected = document.querySelector("#sort").value;
    if (selected == 'lth')
    {
        menData.sort(function (a,b) {
            return a.price - b.price;
        });
        displayData(menData);
    }
    if (selected == 'htl')
    {
        menData.sort(function (a,b) {
            return b.price - a.price;
        });
        displayData(menData);
    }
    if (selected == 'nto')
    {
        menData.sort(function (a,b) {
            return b.publishDate - a.publishDate;
        });
        displayData(menData);
    }
}

function specificProduct() {
    document.querySelector(".products").innerHTML = "";
    
    var selected = document.querySelector("#category").value;

    var filteredList = menData.filter(function (elem) {
            return elem.category == selected;
          });
          displayData(filteredList);
}

function addToCart(elem) {
    console.log(elem);
    cartProducts.push(elem);
    localStorage.setItem("cartData",JSON.stringify(cartProducts));
}








